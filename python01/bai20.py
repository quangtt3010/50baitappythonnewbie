n = float(input("nhập số kwh : "))
tien =  0
if 0<=n<=50:
    tien+=n*1.678
elif 50<n<=100:
    tien+=50*1.678+(n-50)*1.734
elif 100<n<=200:
    tien+=50*1.678+50*1.734+(n-100)*2.014

elif 200<n<=300:
     tien+=50*1.678+50*1.734+100*2.014+(n-200)*2.536
elif 300<n<400:
    tien+=50*1.678+50*1.734+100*2.014+100*2.536+(n-300)*2.834
else:
    tien+=50*1.678+50*1.734+100*2.014+100*2.536+100*2.834+(n-400)*2.927
print(tien)
